# creating mathematical functions
# a is the first number and should be smaller than the second number
# b is the second number and should be greather than the first number
def sum(a, b):
    result = a+b
    return result

def sub(a, b):
    result = b-a
    return result

def div(a, b):
    result = b/a
    return result

def mult(a, b):
    result = int(a) * int(b)
    return result

