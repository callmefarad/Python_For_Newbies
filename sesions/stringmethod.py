# string methods are simply built-in methods or function that can be used
# to manipulate any string.

# strip() method is used to cancel out the white spaces found in a string

# declaring my variable
my_string1 = "this is my string."
my_string2 = "This is my string."
# display the value of the variable without the inclusion of a space.
print(my_string1.strip())

# capitalize() function is used to change the first letter of a word to
# an uppercase
print(my_string1.capitalize())

# casefold() function converts the first letter of a string to lower case
print(my_string2.casefold())

# center() function returns a center string
print(my_string1.center(3))

